def fruit_price(name):
	if(name=='apple'):
		print('Price of '+name+' is Rs 100');
	if(name=='orange'):
		print('Price of '+name+' is Rs 200');
	if(name=='banana'):
		print('Price of '+name+' is Rs 300');
	if(name=='mango'):
		print('Price of '+name+' is Rs 250');
		
name=input('Enter Fruit Name : ');
fruit_price(name);
